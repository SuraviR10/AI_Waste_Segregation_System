def get_earth_message_multilang(context, language='en', waste_type=None, user_name=None):
    """Get Earth message in specified language (en, hi, kn)"""
    import numpy as np
    
    language = language.lower() if language else 'en'
    if language not in EARTH_MESSAGES_MULTILANG:
        language = 'en'
    
    lang_dialogues = EARTH_MESSAGES_MULTILANG[language]
    
    if context == 'welcome':
        return np.random.choice(lang_dialogues['welcome'])
    elif context == 'thinking':
        return np.random.choice(lang_dialogues['thinking'])
    elif context == 'correct':
        return np.random.choice(lang_dialogues['correct'])
    elif context == 'wrong':
        return np.random.choice(lang_dialogues['wrong'])
    elif context == 'emotional':
        return np.random.choice(lang_dialogues.get('emotional', EARTH_DIALOGUES['emotional']))
    elif context == 'daily_tip':
        # For daily tips, use English for now (can expand later)
        return np.random.choice(EARTH_DIALOGUES['daily_tips'])
    elif context == 'impact' and waste_type:
        # Return English impact message (can extend to Hindi/Kannada later)
        if waste_type in EARTH_DIALOGUES.get('impact', {}):
            return np.random.choice(EARTH_DIALOGUES['impact'][waste_type])
    
    return ""